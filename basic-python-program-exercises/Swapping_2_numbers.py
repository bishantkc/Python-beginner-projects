num1 = 5
num2 = 10

print("Value of num1 before swapping: " + str(num1))
print("Value of num2 before swapping: " + str(num2))

variable_x = num1  # variable_x will contain 5
num1 = num2 # num1 value will be num2 value i.e 10
num2 = variable_x   # num2 value will be variable_x i.e 5

print("Value of num1 after swapping: " + str(num1))
print("Value of num2 after swapping: " + str(num2))